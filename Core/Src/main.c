/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	int16_t rotaryPulse = 0;
	uint32_t counter= 0;
	int8_t axisSelection = AXIS_OFF;		// initial selection is OFF
	int8_t magSelection = MAG_0010;		// initial selection 10um


  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USB_DEVICE_Init();
  MX_I2C1_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

  // start TIM4 encoder count
  Encoder(&htim4);	// encoder setup
  enc_Start();	// Encoder read start

  // graphic
  u8g_DrawFunc(&u8g2);
  u8g_Setup();

  // Key handler
  BtnHandler();

  // Sending Key code
  key_Setup();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	  counter ++;

	    // Send HID report

	  // Read button status
	  btn_ReadKeyStatus();
	  // Detect key status edge
	  struct KEYSTATUS sts;
	  btn_SetKeyStatus(&sts);

	  if( (OFF_EDGE == sts.btn1) || (OFF_EDGE == sts.btn2) )
	  {
		  axisSelection += btn_Axis();
		  if( axisSelection < AXIS_OFF ) axisSelection = AXIS_OFF;
		  if( axisSelection > MAX_AXIS ) axisSelection = MAX_AXIS;
		  enc_ResetPulse();
	  }
	  if( (OFF_EDGE == sts.btn3) || (OFF_EDGE == sts.btn4) )
	  {
		  magSelection += btn_MAG();
		  if( magSelection < MAG_0001 ) magSelection = MAG_0001;
		  if( magSelection > MAG_1000 ) magSelection = MAG_1000;
		  key_sendMag(magSelection);
		  enc_ResetPulse();
	  }
	  if( (OFF_EDGE == sts.btn5) )	// Cycle start
	  {
		  // Send Key code
		  key_sendCS();
	  }
	  if( (ON_EDGE == sts.btn6) )	// Feed Hold
	  {
		  // Send Key code
		  key_sendFH();
	  }

		// Read Counter
		rotaryPulse = enc_GetPulse(); // Read rotation level (No update volume value)

		if(/*!(counter%10) &&*/ rotaryPulse != 0)
		{
			int8_t dir = (rotaryPulse<0)?PULSE_MINUS:PULSE_PLUS;

			key_sendPulse(axisSelection, dir );
			enc_SentPulse(dir);	// send command
		}

		// Draw graphic
		u8g_Draw(rotaryPulse, axisSelection, magSelection);

        HAL_Delay(10);

  }
  /* USER CODE END 3 */
}

